      <article>
        <h3>🐶 Abandono de animales</h3>
        <p>
          El abandono es una de las formas más crueles de maltrato.  
          Miles de perros y gatos son dejados en la calle sin alimento ni protección.  
          Estos animales se enfrentan a la desnutrición, enfermedades, atropellos y maltrato por parte de desconocidos.  
          Abandonar a un animal en el Perú es considerado un delito, y tu denuncia puede salvar vidas.
        </p>
        <img src="img/gato1.jpg" alt="Gato abandonado" width="350">
      </article>

      <article>
        <h3>🥩 Falta de alimentación o agua</h3>
        <p>
          Negar alimento y agua a una mascota es una forma de maltrato silenciosa pero muy dañina.  
          Muchos animales son encadenados o encerrados sin acceso a comida o agua limpia durante días, lo que causa sufrimiento y puede llevarlos a la muerte.  
          Toda criatura merece lo básico: alimento, agua y un refugio digno.  
          Si conoces un caso, repórtalo inmediatamente.
        </p>
        <img src="img/perro2.jpg" alt="Perro sin alimento" width="350">
      </article>

      <article>
        <h3>👊 Maltrato físico</h3>
        <p>
          El maltrato físico incluye golpes, patadas, quemaduras, heridas provocadas intencionalmente, así como castigos crueles.  
          Estas agresiones no solo causan dolor físico, sino también traumas psicológicos en los animales, generando miedo y desconfianza hacia los humanos.  
          Es vital denunciar cualquier forma de violencia para protegerlos y hacer cumplir la Ley de Protección y Bienestar Animal en el Perú.
        </p>
        <img src="img/ayuda.jpg" alt="Animal golpeado" width="350">
      </article>

      <article>
        <h3>🐴 Explotación animal</h3>
        <p>
          La explotación animal incluye trabajos forzados (como caballos y burros usados en exceso sin cuidados),  
          peleas clandestinas de perros o gallos, uso de animales en espectáculos crueles y experimentación sin ética.  
          Estas prácticas causan sufrimiento extremo y muchas veces terminan en la muerte del animal.  
          Cada denuncia ayuda a visibilizar estos casos y presionar para que se haga justicia.
        </p>
        <img src="img/caballo.jpg" alt="Caballo explotado" width="350">
